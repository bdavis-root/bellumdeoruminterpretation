<p>
	I appreciate the fact that two dueling GCs sounds, well, fairly crazy. But ask yourself: Really,
	is it much more so than any of the others? The quantum world, and reality itself, are both
	mind-bogglingly strange. I feel strongly that because of that fact, there will never be an
	explanation that does not seem outlandish. All interpretations will seem crazy to a degree, and we
	may have no choice but to pick the ones that seem the <em>least</em> illogical and put them to the
	test. Apart from our ability to do that, perhaps we simply pick the one that sounds the most logical,
	and/or makes us feel the best, to believe.
</p>
