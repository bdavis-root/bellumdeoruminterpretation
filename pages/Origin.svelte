	not apply? Why wouldn’t we have found a much simpler mechanism, instead? If there
	<em>are</em> simpler methods, then the only logical reason for the more-complex option
	<em>must</em> be that those complexities serve multiple purposes, rather than serving only as our reality’s
	LEGO bricks. Therefore, I feel that this is one possible theory that provides logical functions for
	the complexities in question.
</p>
<p>
	The one aspect that really solidified my desire to communicate this to others, though, comes down
	to one deciding factor: from the best of my understanding, the theories this framework is built
	upon do not seem to contradict each other. They all seem to paint the same general picture of
	reality. Any current inconsistencies between them could be resolved in ways that would not exclude
	this theory. And in that sense, from the best I can discern, this theory itself is also (mostly)
	self-consistent.
</p>
<div class="indent-8">
	Let’s try this from yet another angle. If you are an engineer, scientist, or physicist, I present
	you with the following challenge: Design it better. If you assume that our reality is not a
	competition between two dueling GCs, then taking what we know of our universe, how would you have
	improved upon its design in order to make it into one? Design the game under the following
	assumptions:
	<ol class="list-decimal pl-8 pr-8 pt-4 pb-4 indent-2">
		<li>
			The game is a competition to create a universe with the greatest amount of conscious life.
		</li>
		<li>
			The game should be for two GCs with huge amounts of computational power, who can interact with
			the game constructively and destructively.
		</li>
		<li>
			The game should last a very long time, because such beings would have many billions or
			trillions of years at their disposal.
		</li>
		<li>
			The GCs are not permitted to directly communicate with their conscious creations, as that
			would be cheating (an automatic loss).
		</li>
		<li>
			There should be a subtle breadcrumb trail that would allow the created conscious life to
			figure out the reality of the game, since that is the goal. If a GC is able to help conscious
			life survive long enough to understand the truth of the game, the odds of winning increase.
		</li>
	</ol>
</div>
<p>
	Based on these requirements, how would you do it better? I ask because personally, I am at a
	complete loss. I certainly cannot come up with any better way. Viewing the known laws of physics
	from this perspective, I feel that the engineering behind it is beyond absolute and utter
	elegance. It is the embodiment of perfection, in every conceivable way.
</p>
