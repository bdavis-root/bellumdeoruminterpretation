		>cognitive dissonance</LinkInfo
	> associated with my own thought experiment.
</p>
<p>
	I never expected or dreamed that the more research I did, I would find out that the concept is not
	just possible, but that the physics of our universe seem to completely align with what I imagined.
	So well, in fact, that to me, it seems as if our entire reality might have been purpose-built for
	it. I found a universe in a complete state of imbalance, and the theory I came up with looked as
	if it would complete the equation. At that point, I had started to see that this might be a viable
	candidate that could truly explain everything, and my beliefs began to shift.
</p>
<p>
	Then, the more I learned about out the other interpretations of quantum mechanics, I felt that the
	one that I had come up with explains the absolute most in terms of logical reasons behind the
	strange behaviors of physics and reality, and allows for the complete unification of what we know
	about physical reality with both philosophy and faith. By this time, I was beginning to reshape my
	own beliefs with what I had found. However, I was not yet at the stage that I truly wished to try
	and change anyone else’s mind, other than anecdotal banter with friends and family. But as I
	continued to explore further, more consistencies still kept occurring to me.
</p>
<p>
	One issue I have considered (at great length) is that by looking at physics from a reversed angle,
	meaning that I am trying to figure out if an idea is possible within the currently-known laws and
	theories, I run the risk of introducing a rather large amount of cognitive bias. I could simply be
	seeing functions and patterns that do not truly exist, simply because that is what I am looking
	for. While I admit that is true, the reasons behind <em>why</em> the quantum world behaves the way
	it does has stumped the greatest minds of our planet, including intellectual giants such as
	Einstein, Bohr, Feynman, Heisenberg, Bell, Hawking, and so many more, for over a century. The very
	people who discovered the quantum nature of our universe never expected reality to behave in that
	way. The idea of particles being physically real, and having defined and indivisible physical
	pieces, was what the best scientists of the 19<sup>th</sup> century expected to find once their tools
	were advanced enough to divide that matter and view it. In the early part of the century after, what
	they found instead was interesting, but also both confusing and discomforting.
</p>
<p>
	So, then: why does this quantum world exist? Is it the <em>only</em> possible way our macro
	reality could have been described? Or, could an extremely similar version of our current macro
	reality be constructed without such a complicated and strange underlying mechanism? Put another
	way, if we conclude that there may have been multiple ways to “skin the cat” and come up with a
	very similar result, then why would
	<LinkInfo
