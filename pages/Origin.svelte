<h2>THE ORIGIN OF BDI</h2>

<p>
	I feel that it is important to explain how this theory originated, because doing so might allow
	you to better understand the process from my perspective.
</p>
<p>
	As much as I hate to admit it - I just turned 40 years old, and I am an anime geek. Supposedly,
	that’s more <LinkInfo
		type="Link"
		title="YouTube > How Did Anime Go From Geek to Cool? (PBS Voices | Subcultured)"
		href="https://www.youtube.com/watch?v=oSCj8H4TGTo">culturally-accepted</LinkInfo
	> these days, but I remain sheepish. Regardless, one thing I truly enjoy about the genre is that because
	its plots are usually not at all grounded in reality, it can allow you to consider possibilities that
	you might not otherwise. And in this instance, that is exactly what occurred. One day in the not-so-distant
	past, I was watching a series while my wife was busy working on her college classes. I wish I knew
	the exact date, but (since Netflix decides to erase your previous viewing history if you rewatch something,)
	I do not. What I <em>do</em> know is that it was some time after December 11<sup>th</sup> 2019, since
	that was the date the episode that I was watching was originally available.
</p>
<p>
	In the <LinkInfo
		type="Link"
		title="Netflix > The Seven Deadly Sins"
		href="https://www.netflix.com/title/80050063">series</LinkInfo
	>, there are two opposing gods that command the beings under their reign to battle each other. The
	Supreme Deity, who commands the Goddess Race, is in direct conflict with the Demon King, who
	commands the Demon Clan. One might think that the Goddess Race would be benevolent and good, while
	the Demon Clan would be underhanded and merciless. But as it turns out, (and is common in the
	genre), both sides fight just as dirty. Most belligerents on both sides are willing to exterminate
	the other without pause or mercy, usually because each side is seeking vengeance for past
	atrocities wrought by the hands of their enemy.
</p>
<p>
	The episode I was watching explains how the (up-to-then) four seasons of the show are connected. I
	don’t wish to spoil the plot for anyone in case they wish to watch it, because I highly recommend
	the series if you can get over the overt cheesiness of it. However, the culmination of the plot
	tells the single most tragic love story I have heard in my entire life. In my opinion, the
	suffering in Shakespeare’s tragedies can’t hold a candle to this story. For a moment, it affected
	me emotionally to the point that I actually had to pause the show to process it, mostly because I
	was unprepared for the fact that a cartoon geared for a 14+ audience might include such a tragic
	plot.
</p>
<p>In an episode prior to the one I was watching that day, one of the main characters says this:</p>
<p>
	<strong
		>“As bad as the world may seem, true evil isn’t that common. From what I’ve seen, through those
		I’ve battled till now, everyone has a reason for doing what they do. A guy could be scum, at
		least from my perspective, but that same guy might be someone irreplaceable to my sister. I
		mean, it’s the same for you, isn’t it?”</strong
	>
</p>
<p>
	At that moment, I recalled this quote. Coupled with the plot of two opposing gods, it caused what
	felt like a fuse blowing in my brain. As I sat there, I felt a chill roll down my spine, and it
	seemed as if time stopped for a brief moment. I considered the one man most of us think of when
	the concept of evil is brought up. If you were to ask five random strangers on the street of any
	major city who they thought the most evil person in all of history was, who do you imagine the
	majority, if not all of them, would name?
</p>
<p>
	I imagined that a malevolent higher consciousness could have exploited Adolf Hitler’s sense of
	injustices imposed on Germany after World War I, and his sins of racial/national pride and greed
	for land and resources, to justify committing horrific acts of evil. And to him, he did those
	things with the best intentions. In his own mind, he felt he was absolutely justified in
	everything he did because it was for the good of his own people. By extension, once he started
	down his path, motivated by that pride, wrath, and greed, that allowed for his fellow countrymen
	to also be easily exploited in his wake, since they, too, (either already or eventually) were
	convinced of the same beliefs and justifications. The cascade of those factors could have led to
	what is historically accepted as the worst acts of brutality and genocide in human history.
</p>
<p>
	As I reflected on that possibility, I realized that two opposing gods, each within their own
	separate-but-linked universes, would explain just about <em>everything</em> in terms of
	philosophy, human behavior, and the human condition. It would both resolve the problem of evil
	<em>and</em> allow for God, perhaps even a loving one, to exist at the same time. The realization
	shattered my sense of reality, because it opened the possibility for religion, philosophy, and
	logic to all coexist. But surely, the laws of physics should dictate that this is impossible,
	right? Could another entire universe made of antimatter <em>really</em> exist?
</p>
<p>
	From there, and over the past 3+ years, I began trying to understand what I could of the extremely
	complex laws of physics, and how that thought could be either possible or impossible in terms of
	our actual, testable reality. From the point I started, I knew that relativity imposed the
	restrictions of locality and the speed of light upon us, that quantum mechanics and entanglement
	were extremely strange phenomena that seemed to violate that same locality and speed limit, that
	antimatter exists, and there was baryonic asymmetry in our universe.
</p>
<p>
	However, I fully expected that the more I researched physics, the more I would find that I would
	have to bend or break the rules of reality for this ultra-bizarre dueling gods theory to make any
	sense whatsoever. Quite honestly, that is precisely what I was looking for. I simply would never
	accept a system of belief that would contradict science. If I could prove to myself that the
	construct was impossible (or even highly improbable) within the laws of physics, I could dismiss
	it and return to my agnostic beliefs. It would mean that my worldview could remain what it was,
	and I would not have to change. I could go back to my quasi-nihilistic ideology wherein God almost
	certainly does not exist, we all evolved and exist due to countless throws of the dice and random
	happenstance, and the only thing that really links any of us is that we are all made up from the
	same cosmic goo. Above all, I could go back to believing that the only thing that really mattered
	in anyone’s life, including my own, is individual happiness. I could go back to being the selfish
	person I had been my entire life. Somewhat comically, I was experiencing effects of <LinkInfo
		title="Wikipedia > Cognitive Dissonance"
		href="https://en.wikipedia.org/wiki/Cognitive_dissonance#:~:text=In%20the%20field,reduce%20their%20discomfort."
